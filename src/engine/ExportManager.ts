export async function exportScene(
  sceneManager: SceneManager,
  format: ExportFormat,
): Promise<void> {
  switch (format) {
    case 'glb':
      await exportGLB(sceneManager);
      break;
    case 'obj':
      exportOBJ(sceneManager);
      break;
    case 'fbx':
      exportFBX(sceneManager);
      break;
  }
}
